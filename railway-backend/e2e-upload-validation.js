// ?? setup: login + register + create pet ?????????????????????????????????????
async function setup(base) {
  const { json: lr } = await jsonReq(base, "POST", "/api/auth/login",
    { identifier: "admin@nagarnigam.gov.in", password: "Admin@123" });
  const saToken = lr.token;

  const { json: cities } = await jsonReq(base, "GET", "/api/geo/cities");
  const city  = cities.find(c => c.name === "Jaipur") || cities[0];
  const { json: nigams } = await jsonReq(base, "GET", `/api/geo/nigams?cityId=${city.id}`);
  const nigam = nigams[0];
  const { json: zones }  = await jsonReq(base, "GET", `/api/geo/zones?nigamId=${nigam.id}`);
  const zone  = zones[0];
  const { json: wards }  = await jsonReq(base, "GET", `/api/geo/wards?zoneId=${zone?.id}`);
  const ward  = wards[0];

  const mobile = "9" + Date.now().toString().slice(-9);
  const { json: reg } = await jsonReq(base, "POST", "/api/auth/register", {
    name: "Upload Test User", mobile,
    email: `${mobile}@test.com`, password: "Test@1234",
    address: "Upload Test Address",
    cityId: city.id, nigamId: nigam.id, zoneId: zone?.id, wardId: ward?.id,
  });
  const citizenToken = reg.token;

  const { json: pet } = await jsonReq(base, "POST", "/api/pets", {
    name: "UploadTestPet", species: "dog", breed: "Beagle",
    colour: "Brown", gender: "male", dateOfBirth: "2023-01-01",
  }, citizenToken);

  return { saToken, citizenToken, petId: pet.id };
}

// ?? upload test ???????????????????????????????????????????????????????????????
async function testUploads(label, base) {
  console.log(`\n${"?".repeat(50)}`);
  console.log(`${label}  (${base})`);
  console.log("?".repeat(50));

  let petId, citizenToken;
  try {
    ({ petId, citizenToken } = await setup(base));
    check(`Setup: pet created (id=${petId})`, !!petId);
  } catch (e) {
    check("Setup", false, e.message);
    return;
  }

  // ?? Photo upload ???????????????????????????????????????????????????????????
  console.log("\n  [Photo Upload]");
  {
    const { status, json } = await uploadReq(base, `/api/pets/${petId}/upload-photo`, "photo", citizenToken);
    check("upload-photo returns 200",       status === 200, `status=${status} body=${JSON.stringify(json)}`);
    check("upload-photo message correct",   json.message === "Photo uploaded.", json.message);
    check("upload-photo url set",           !!json.url, json.url);
  }

  // ?? Verify photo_url persisted in DB ?????????????????????????????????????
  {
    const { json } = await jsonReq(base, "GET", `/api/pets/${petId}`, null, citizenToken);
    check("photo_url persisted in pet record", !!json.photo_url, `got: ${json.photo_url}`);
  }

  // ?? Certificate upload ????????????????????????????????????????????????????
  console.log("\n  [Certificate Upload]");
  {
    const { status, json } = await uploadReq(base, `/api/pets/${petId}/upload-certificate`, "certificate", citizenToken);
    check("upload-certificate returns 200",     status === 200, `status=${status} body=${JSON.stringify(json)}`);
    check("upload-certificate message correct", json.message === "Certificate uploaded.", json.message);
    check("upload-certificate url set",         !!json.url, json.url);
  }

  // ?? Verify certificate_url persisted ?????????????????????????????????????
  {
    const { json } = await jsonReq(base, "GET", `/api/pets/${petId}`, null, citizenToken);
    check("certificate_url persisted in pet record", !!json.certificate_url, `got: ${json.certificate_url}`);
  }

  // ?? Reject invalid file type ??????????????????????????????????????????????
  console.log("\n  [Reject Invalid File Type]");
  {
    const fd = new globalThis.FormData();
    fd.append("photo", new globalThis.Blob(["not an image"], { type: "text/plain" }), "bad.txt");
    const r = await fetch(`${base}/api/pets/${petId}/upload-photo`, {
      method: "POST",
      headers: { Authorization: `Bearer ${citizenToken}` },
      body: fd,
    });
    check("Invalid file type rejected (400/500)", r.status === 400 || r.status === 500,
      `got ${r.status}`);
  }
}

