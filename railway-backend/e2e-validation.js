const BASE = "http://localhost:3000";
let pass = 0, fail = 0;

async function req(method, path, body, token) {
  const opts = {
    method,
    headers: { "Content-Type": "application/json", ...(token ? { Authorization: `Bearer ${token}` } : {}) },
  };
  if (body) opts.body = JSON.stringify(body);
  const r = await fetch(`${BASE}${path}`, opts);
  const text = await r.text();
  let json;
  try { json = JSON.parse(text); } catch { json = { _raw: text }; }
  return { status: r.status, json };
}

function check(label, condition, detail = "") {
  if (condition) {
    console.log(`  ? PASS: ${label}`);
    pass++;
  } else {
    console.error(`  ? FAIL: ${label}${detail ? " — " + detail : ""}`);
    fail++;
  }
}

// ?? helpers ???????????????????????????????????????????????????????????????????
async function loginAs(identifier, password) {
  const { status, json } = await req("POST", "/api/auth/login", { identifier, password });
  if (status !== 200) throw new Error(`Login failed for ${identifier}: ${json.error || status}`);
  return json.token;
}

async function registerCitizen(mobile, name) {
  // Create a fresh citizen tied to Jaipur ? JMC ? Zone 1 ? Ward
  // First get IDs
  const { json: cities } = await req("GET", "/api/geo/cities");
  const city = cities.find(c => c.name === "Jaipur") || cities[0];
  const { json: nigams } = await req("GET", `/api/geo/nigams?cityId=${city.id}`);
  const nigam = nigams[0];
  const { json: zones }  = await req("GET", `/api/geo/zones?nigamId=${nigam.id}`);
  const zone = zones[0];
  const { json: wards }  = await req("GET", `/api/geo/wards?zoneId=${zone.id}`);
  const ward = wards[0];

  const { status, json } = await req("POST", "/api/auth/register", {
    name,
    mobile,
    email: `${mobile}@test.com`,
    password: "Test@1234",
    address: "123 Test Street",
    cityId:  city.id,
    nigamId: nigam.id,
    zoneId:  zone?.id,
    wardId:  ward?.id,
  });
  if (status !== 201) throw new Error(`Register failed: ${json.error || status}`);
  return { token: json.token, user: json.user, nigam };
}

// ?????????????????????????????????????????????????????????????????????????????
async function main() {
  console.log("?????????????????????????????????????????????????");
  console.log(" AFP End-to-End Validation");
  console.log("?????????????????????????????????????????????????\n");

  // ?? 0. Health check ??????????????????????????????????????????????????????
  console.log("?? 0. Health Check ??????????????????????????????");
  {
    const { status, json } = await req("GET", "/health");
    check("API is reachable", status === 200);
    check("DB is connected", json.db === "connected", json.db);
  }

  // ?? 1. Super Admin login ?????????????????????????????????????????????????
  console.log("\n?? 1. Super Admin Login ?????????????????????????");
  let saToken;
  try {
    saToken = await loginAs("admin@nagarnigam.gov.in", "Admin@123");
    check("Super admin login succeeds", !!saToken);
  } catch (e) {
    check("Super admin login succeeds", false, e.message);
    console.error("Cannot continue without super admin token"); process.exit(1);
  }

  // ?? 2. Read initial fees from Nigam ?????????????????????????????????????
  console.log("\n?? 2. Read Initial Nigam Fees ???????????????????");
  let nigamId, originalFees;
  {
    const { json: cities } = await req("GET", "/api/geo/cities");
    const jaipur = cities.find(c => c.name === "Jaipur") || cities[0];
    check("At least one city exists", cities.length > 0);

    const { status, json: nigams } = await req("GET", `/api/geo/nigams?cityId=${jaipur.id}`);
    check("Nigams endpoint returns 200", status === 200);
    check("Nigams list non-empty", nigams.length > 0);
    check("Nigam has registration_fee", nigams[0]?.registration_fee != null,
      JSON.stringify(nigams[0]));
    check("Nigam has renewal_fee",      nigams[0]?.renewal_fee != null);
    check("Nigam has transfer_fee",     nigams[0]?.transfer_fee != null);

    nigamId = nigams[0]?.id;
    originalFees = {
      registration_fee: Number(nigams[0]?.registration_fee),
      renewal_fee:      Number(nigams[0]?.renewal_fee),
      transfer_fee:     Number(nigams[0]?.transfer_fee),
    };
    console.log(`  ??  Current fees: reg=?${originalFees.registration_fee} renew=?${originalFees.renewal_fee} txn=?${originalFees.transfer_fee}`);
  }

  // ?? 3. Admin — read nigams/all with fees ?????????????????????????????????
  console.log("\n?? 3. Admin Read Nigams/All ?????????????????????");
  {
    const { json: cities } = await req("GET", "/api/geo/cities");
    const jaipur = cities.find(c => c.name === "Jaipur") || cities[0];
    const { status, json } = await req("GET", `/api/geo/nigams/all?cityId=${jaipur.id}`, null, saToken);
    check("Admin nigams/all returns 200", status === 200);
    check("Admin nigams/all has fee fields", json[0]?.registration_fee != null,
      JSON.stringify(json[0]));
    check("registration_fee value correct", Number(json[0]?.registration_fee) === originalFees.registration_fee);
    check("renewal_fee value correct",      Number(json[0]?.renewal_fee)      === originalFees.renewal_fee);
    check("transfer_fee value correct",     Number(json[0]?.transfer_fee)     === originalFees.transfer_fee);
  }

  // ?? 4. Admin — update fees ????????????????????????????????????????????????
  console.log("\n?? 4. Super Admin Updates Fees ?????????????????");
  const newFees = { registration_fee: 250, renewal_fee: 180, transfer_fee: 120 };
  {
    const { status, json } = await req(
      "PUT", `/api/geo/nigams/${nigamId}`,
      { name: "Jaipur Municipal Corporation", is_active: true, ...newFees },
      saToken
    );
    check("PUT /nigams/:id returns 200", status === 200, json.error);
    check("registration_fee updated to 250", Number(json.registration_fee) === 250, json.registration_fee);
    check("renewal_fee updated to 180",      Number(json.renewal_fee)      === 180, json.renewal_fee);
    check("transfer_fee updated to 120",     Number(json.transfer_fee)     === 120, json.transfer_fee);
  }

  // ?? 5. Verify fees persisted in public API ????????????????????????????????
  console.log("\n?? 5. Verify Updated Fees Visible in Public API ?");
  {
    const { json: cities } = await req("GET", "/api/geo/cities");
    const jaipur = cities.find(c => c.name === "Jaipur") || cities[0];
    const { json: nigams } = await req("GET", `/api/geo/nigams?cityId=${jaipur.id}`);
    check("Public nigams shows updated reg fee 250", Number(nigams[0]?.registration_fee) === 250,
      `got ${nigams[0]?.registration_fee}`);
    check("Public nigams shows updated renewal fee 180", Number(nigams[0]?.renewal_fee) === 180,
      `got ${nigams[0]?.renewal_fee}`);
    check("Public nigams shows updated transfer fee 120", Number(nigams[0]?.transfer_fee) === 120,
      `got ${nigams[0]?.transfer_fee}`);
    console.log(`  ??  New fees live: reg=?${nigams[0]?.registration_fee} renew=?${nigams[0]?.renewal_fee} txn=?${nigams[0]?.transfer_fee}`);
  }

  // ?? 6. Register a new citizen ?????????????????????????????????????????????
  console.log("\n?? 6. Register New Citizen ??????????????????????");
  let citizenToken, registeredNigam;
  const testMobile = "9" + Date.now().toString().slice(-9);
  try {
    const result = await registerCitizen(testMobile, "Test Citizen E2E");
    citizenToken = result.token;
    registeredNigam = result.nigam;
    check("Citizen registration succeeds", !!citizenToken);
    console.log(`  ??  Citizen mobile: ${testMobile}, nigam: ${registeredNigam?.name}`);
  } catch (e) {
    check("Citizen registration succeeds", false, e.message);
    process.exit(1);
  }

  // ?? 7. Pet registration (payment-disabled flow) ???????????????????????????
  console.log("\n?? 7. Pet Registration (Payment Disabled) ???????");
  let petId;
  {
    const { status, json } = await req("POST", "/api/pets", {
      name:        "Bruno",
      species:     "dog",
      breed:       "Labrador",
      colour:      "Golden",
      gender:      "male",
      dateOfBirth: "2022-03-15",
      // No paymentId or txnRef — payment disabled
    }, citizenToken);
    check("POST /api/pets returns 201", status === 201, json.error);
    check("Pet has id",                 !!json.id);
    check("Pet has pet_id assigned",    !!json.pet_id, json.pet_id);
    check("Pet name is Bruno",          json.name === "Bruno");
    check("Pet status is pending",      json.registration_status === "pending");
    check("No payment required",        !json.error);
    petId = json.id;
    console.log(`  ??  Pet registered: id=${json.id}, pet_id=${json.pet_id}, status=${json.registration_status}`);
  }

  // ?? 8. Citizen can read their pet ????????????????????????????????????????
  console.log("\n?? 8. Citizen Reads Own Pet ?????????????????????");
  {
    const { status, json } = await req("GET", `/api/pets/${petId}`, null, citizenToken);
    check("GET /api/pets/:id returns 200", status === 200, json.error);
    check("Pet name correct",             json.name === "Bruno");
    check("Status still pending",         json.registration_status === "pending");
  }

  // ?? 9. Citizen sees pet in their list ????????????????????????????????????
  console.log("\n?? 9. Citizen My Pets List ??????????????????????");
  {
    const { status, json } = await req("GET", "/api/pets/my", null, citizenToken);
    check("GET /api/pets/my returns 200",     status === 200);
    check("My pets list contains Bruno",      json.some(p => p.name === "Bruno"));
    check("Pet status is pending in list",    json.find(p => p.name === "Bruno")?.registration_status === "pending");
  }

  // ?? 10. Ward Admin approves the pet ??????????????????????????????????????
  console.log("\n?? 10. Ward Admin Approves Pet ??????????????????");
  let wardAdminToken;
  {
    // Try to find an existing ward admin, or use super admin to approve
    // Super admin has super_admin role – requireRole("ward_admin") passes for it
    wardAdminToken = saToken;
    const { status, json } = await req(
      "PATCH", `/api/pets/${petId}/approve`, { note: "E2E approved" }, wardAdminToken
    );
    check("PATCH /approve returns 200", status === 200, json.error);
    check("Response confirms approval", json.message?.includes("Approved") || json.id === petId,
      JSON.stringify(json));
  }

  // ?? 11. Pet status is now approved ???????????????????????????????????????
  console.log("\n?? 11. Pet Status After Approval ????????????????");
  {
    const { status, json } = await req("GET", `/api/pets/${petId}`, null, citizenToken);
    check("Pet status is approved", json.registration_status === "approved",
      `got: ${json.registration_status}`);
  }

  // ?? 12. Reset fees to original values ????????????????????????????????????
  console.log("\n?? 12. Reset Fees to Original Values ????????????");
  {
    const { status, json } = await req(
      "PUT", `/api/geo/nigams/${nigamId}`,
      { name: "Jaipur Municipal Corporation", is_active: true, ...originalFees },
      saToken
    );
    check("Fees reset returns 200", status === 200, json.error);
    check("registration_fee reset", Number(json.registration_fee) === originalFees.registration_fee);
    check("renewal_fee reset",      Number(json.renewal_fee)      === originalFees.renewal_fee);
    check("transfer_fee reset",     Number(json.transfer_fee)     === originalFees.transfer_fee);
    console.log(`  ??  Fees restored: reg=?${json.registration_fee} renew=?${json.renewal_fee} txn=?${json.transfer_fee}`);
  }

  // ?? 13. Unauthorized fee update blocked ??????????????????????????????????
  console.log("\n?? 13. Security: Citizen Cannot Update Fees ?????");
  {
    const { status } = await req(
      "PUT", `/api/geo/nigams/${nigamId}`,
      { registration_fee: 999 },
      citizenToken  // citizen token — should be rejected
    );
    check("Citizen PUT nigams returns 401 or 403", status === 401 || status === 403,
      `got ${status}`);
  }

  // ?? Summary ???????????????????????????????????????????????????????????????
  console.log("\n?????????????????????????????????????????????????");
  console.log(` RESULTS: ${pass} passed  |  ${fail} failed`);
  console.log("?????????????????????????????????????????????????");
  if (fail > 0) process.exit(1);
}

main().catch(e => { console.error("Fatal:", e.message); process.exit(1); });
